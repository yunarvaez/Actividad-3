    private void btnEncontrarMayorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEncontrarMayorActionPerformed
        // TODO add your handling code here:
        int mayor;

        String textoNumeros = txtNumeros.getText();
        String[] elementos = textoNumeros.split(" ");
    
        int[] numeros = new int[elementos.length];
        for (int i = 0; i < elementos.length; i++) {
        numeros[i] = Integer.parseInt(elementos[i]);
    }
        
        ProcesarNumeros procesar = new ProcesarNumeros();
        mayor = procesar.encontrarMayor(numeros);
        txtMayor.setText(String.valueOf(mayor));
    }//GEN-LAST:event_btnEncontrarMayorActionPerformed

